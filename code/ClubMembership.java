    private void showCustomerList(DefaultTableModel model){
        try {
           
             List<Member> arraylistData = file.readData();
            String rowData[] = new String[arraylistData.size()];
            for (int i = 0; i < arraylistData.size(); i++) {
            rowData[0] = arraylistData.get(i).getMembership_Number();
            rowData[2] = arraylistData.get(i).getFirst_name();
            rowData[1] = arraylistData.get(i).getLast_name();
            rowData[3] = arraylistData.get(i).getDate();
            rowData[4] = arraylistData.get(i).getGender();
            rowData[5] = arraylistData.get(i).getAddress();
            rowData[6] = arraylistData.get(i).getPhone_no();
            rowData[7] = arraylistData.get(i).ageCalculate(rowData[3]); //
            rowData[8] = arraylistData.get(i).getMemberType();
            rowData[9] = arraylistData.get(i).getStartDate();
            rowData[10] = arraylistData.get(i).endDateCalculate(rowData[9], rowData[8]);
            rowData[11] = arraylistData.get(i).feesCalculate(rowData[8]);
            model.addRow(rowData);
            } 
        } catch (IOException ex) {
            Logger.getLogger(ClubMembership.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
