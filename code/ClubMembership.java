    private void btnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCustomerActionPerformed
        // TODO add your handling code here:
        if(!(textFirstName.getText().isEmpty() && textLastName.getText().isEmpty() && textDateOfBirth.getText().isEmpty())){       //Validation
            
            DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
            Member member = new Member();
            Visitor visitor = new Visitor();
            if (comboCustomerType.getSelectedItem().toString() == "Vistor"){
                file.writeList(visitor.toString());
            }
            //System.out.println(getSelectedRadioValue(groupGender));
            member.Member(textFirstName.getText(), textLastName.getText(), textDateOfBirth.getText(), getSelectedRadioValue(groupGender), textareaAddress.getText(), textContact.getText(),textStartDate.getText(), comboCustomerType.getSelectedItem().toString());
            file.writeList(member.printAll());
            String rowData[] = new String[12];
            rowData[0] = member.getMembership_Number();
            rowData[2] = member.getFirst_name();
            rowData[1] = member.getLast_name();
            rowData[3] = member.getDate();
            rowData[4] = member.getGender();
            rowData[5] = member.getAddress();
            rowData[6] = member.getPhone_no();
            rowData[7] = member.getAge();
            rowData[8] = member.feesCalculate(member.getMemberType());
            rowData[9] = member.getStartDate();
            rowData[11] = member.getMemberType();
            rowData[10] = member.endDateCalculate(rowData[9],rowData[11]);
            //System.out.println(rowData[7]);
            model.addRow(rowData);
            clearFields();
            model.fireTableDataChanged();
        }
        else{
            JOptionPane.showMessageDialog(new JFrame(),"Fields are still empty","Alert",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddCustomerActionPerformed

