    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        int selectedrowindex = tableCustomer.getSelectedRow();
        textFirstName.setText(model.getValueAt(selectedrowindex, 1).toString());
        textLastName.setText(model.getValueAt(selectedrowindex, 2).toString());
        textDateOfBirth.setText(model.getValueAt(selectedrowindex, 3).toString());
        textareaAddress.setText(model.getValueAt(selectedrowindex, 5).toString());
        if("Male".equals(model.getValueAt(selectedrowindex, 4).toString()) || "male".equals(model.getValueAt(selectedrowindex, 4).toString())){
            radioMale.setSelected(true);
        }else{
            radioFemale.setSelected(true);
        }
    }//GEN-LAST:event_tableCustomerMouseClicked

