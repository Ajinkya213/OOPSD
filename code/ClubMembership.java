    private void btnDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCustomerActionPerformed
       DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
       int selectedrow = tableCustomer.getSelectedRow();
       model.removeRow(selectedrow);
        try {
            // TODO add your handling code here:
            arraylistData = file.readData();
        } catch (IOException ex) {
            Logger.getLogger(ClubMembership.class.getName()).log(Level.SEVERE, null, ex);
        }
          for(int i = 0; i < arraylistData.size(); i++){
            if(textFirstName.getText().equals(arraylistData.get(i).getLast_name())){
                  arraylistData.remove(i);
                  file.removeList(arraylistData);
            }
        }
          model.fireTableDataChanged();
    }//GEN-LAST:event_btnDeleteCustomerActionPerformed

