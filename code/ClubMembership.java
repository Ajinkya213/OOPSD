    private void btnExportCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportCSVActionPerformed
        // TODO add your handling code here:
        JFrame frame = new JFrame();
        JFileChooser chooser = new JFileChooser();
        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        
        chooser.setDialogTitle("Save the file to");
        int save = chooser.showSaveDialog(frame);
        
        if(save == JFileChooser.APPROVE_OPTION){
            try{
                FileWriter filewrite = new FileWriter(new File(chooser.getSelectedFile()+".csv"));
                for(int i = 0; i < model.getRowCount(); i++){
                    for (int j = 0 ; j< model.getColumnCount(); j++){
                        filewrite.write(model.getValueAt(i, j).toString()+", ");
                    }
                    filewrite.write('\n');
                }
            } catch (IOException ex) {
                Logger.getLogger(ClubMembership.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnExportCSVActionPerformed
  
    
